// Los métodos en JavaScript son funciones predefinidas 
// que se utilizan para realizar tareas específicas. 
// Estos métodos se pueden aplicar a diferentes tipos de objetos, 
// como cadenas, arreglos, números, etc.

length:
// El método  length  en JavaScript se utiliza para obtener 
// la longitud de una cadena de texto o el número 
// de elementos en un arreglo.  
 
// Cuando se aplica a una cadena de texto, el método  
// length  devuelve el número de caracteres 
// que contiene la cadena. 

// Por ejemplo:

const texto = "Hola";
console.log(texto.length); // Salida: 4

// Cuando se aplica a un arreglo, el método  length  
// devuelve el número de elementos que contiene el arreglo. 

// Por ejemplo:

const arreglo = [1, 2, 3, 4, 5];
console.log(arreglo.length); // Salida: 5
// El método  length  es útil para realizar operaciones 
// basadas en la cantidad de elementos de una cadena 
// o un arreglo, como recorrerlos en un bucle  for  
// o verificar si están vacíos.